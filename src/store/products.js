export const useProductsStore = defineStore("products", () => {
    // --- reactive catalogue keyed by category --------------------------------
    const catalogue = reactive({
        [CATEGORIES.CONCENTRATE]: CONCENTRATE,
        [CATEGORIES.SLUSHY]: SLUSHY,
    });

    // --- counters (flat map id â†’ qty) ---------------------------------------
    const counts = reactive(
        [...CONCENTRATE, ...SLUSHY].reduce((acc, p) => ({ ...acc, [p.id]: 0 }), {})
    );

    const labelsPerRow = reactive({ value: 3 });
    const activeCategory = reactive({ value: CATEGORIES.CONCENTRATE });

    // --- getters -------------------------------------------------------------
    const activeItems = computed(() => catalogue[activeCategory.value]);

    const total = computed(() =>
        activeItems.value.reduce((s, p) => s + counts[p.id], 0)
    );

    // --- page calculations ---------------------------------------------------
    const LABELS_PER_PAGE = {
        [CATEGORIES.CONCENTRATE]: 24,
        [CATEGORIES.SLUSHY]: 12,
    };

    const totalPages = computed(() => {
        const currentTotal = total.value;
        const labelsPerPage = LABELS_PER_PAGE[activeCategory.value];
        return currentTotal > 0 ? Math.ceil(currentTotal / labelsPerPage) : 0;
    });

    const isPageFull = computed(() => {
        const currentTotal = total.value;
        const labelsPerPage = LABELS_PER_PAGE[activeCategory.value];
        return currentTotal > 0 && currentTotal % labelsPerPage === 0;
    });

    const labelsOnCurrentPage = computed(() => {
        const currentTotal = total.value;
        const labelsPerPage = LABELS_PER_PAGE[activeCategory.value];
        return currentTotal > 0 ? currentTotal % labelsPerPage || labelsPerPage : 0;
    });

    // --- actions -------------------------------------------------------------
    const increment = (id) => {
        counts[id] += 1;
    };
    const incrementBy = (id, amount) => {
        counts[id] += amount;
    };
    const decrement = (id) => {
        if (counts[id] > 0) counts[id] -= 1;
    };
    const setCount = (id, n) => {
        counts[id] = Math.max(0, n);
    };
    const reset = (id) => {
        counts[id] = 0;
    };
    const resetAll = () => {
        Object.keys(counts).forEach(id => {
            counts[id] = 0;
        });
    };
    const setLabelsPerRow = (n) => {
        labelsPerRow.value = Math.min(Math.max(parseInt(n, 10) || 1, 1), 4);
    };

    const setActiveCategory = (cat) => {
        activeCategory.value = cat;
    };

    return {
        /* state */ counts,
        labelsPerRow,
        activeCategory,
        /* refs */ catalogue,
        activeItems,
        /* consts */ CATEGORIES,        /* getters */ total,
        totalPages,
        isPageFull,
        labelsOnCurrentPage,
        /* actions */ increment,
        incrementBy,
        decrement,
        setCount,
        reset,
        resetAll,
        setLabelsPerRow,
        setActiveCategory,
    };
});
